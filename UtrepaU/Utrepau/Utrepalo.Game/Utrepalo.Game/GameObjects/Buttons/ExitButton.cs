using System;
using Microsoft.Xna.Framework;
using Microsoft.Xna.Framework.Graphics;

namespace Utrepalo.Game.GameObjects.Buttons
{
    using Microsoft.Xna.Framework.Content;
    using Game = Microsoft.Xna.Framework.Game;

    public class ExitButton : GameObject
    {
        
        public ExitButton(Texture2D objTexture,Rectangle sourceRectangle, SpriteBatch spriteBatch,Game game) : base(objTexture, new Rectangle(700, 600, 100, 100), sourceRectangle, spriteBatch,game)
        {
        }
        public override void LoadContent(ContentManager content)
        {
            this.objTexture = content.Load<Texture2D>("images/exit-button2");
        }

        public override void Update(GameTime gameTime)
        {
            this.SourceRectangle = new Rectangle(0, 0, 100, 100);
            base.Update(gameTime);
        }

        public override void Draw(SpriteBatch spriteBatch)
        {
            spriteBatch.Draw(this.objTexture,this.DrowingRectangle,this.sourcerRectangle,Color.White);

        }

        public override void RespondToCollision(GameObject hitObject)
        {
            throw new NotImplementedException();
        }

    }
}
